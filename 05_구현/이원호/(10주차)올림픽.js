// [문제 이름]
// : 올림픽

// [문제 설명]
// : 올림픽은 참가에 의의가 있기에 공식적으로는 국가간 순위를 정하지 않는다. 그러나, 많은 사람들이 자신의 국가가 얼마나 잘 하는지에 관심이 많기 때문에 비공식적으로는 국가간 순위를 정하고 있다. 두 나라가 각각 얻은 금, 은, 동메달 수가 주어지면, 보통 다음 규칙을 따라 어느 나라가 더 잘했는지 결정한다.
//  금메달 수가 더 많은 나라
//  금메달 수가 같으면, 은메달 수가 더 많은 나라
//  금, 은메달 수가 모두 같으면, 동메달 수가 더 많은 나라
//  각 국가는 1부터 N 사이의 정수로 표현된다. 한 국가의 등수는 (자신보다 더 잘한 나라 수) + 1로 정의된다. 만약 두 나라가 금, 은, 동메달 수가 모두 같다면 두 나라의 등수는 같다. 예를 들어, 1번 국가가 금메달 1개, 은메달 1개를 얻었고, 2번 국가와 3번 국가가 모두 은메달 1개를 얻었으며, 4번 국가는 메달을 얻지 못하였다면, 1번 국가가 1등, 2번 국가와 3번 국가가 공동 2등, 4번 국가가 4등이 된다. 이 경우 3등은 없다.
//  각 국가의 금, 은, 동메달 정보를 입력받아서, 어느 국가가 몇 등을 했는지 알려주는 프로그램을 작성하시오.

// [문제 링크]
// : https://www.acmicpc.net/problem/8979

// k = 3 (순위를 알고싶은 국가)
// medals = [[1,1,2,0],[2,0,1,0],[3,0,1,0],[4,0,0,1]]

function solution(k, medals) {
  medals.sort((a, b) => b[3] - a[3]);
  medals.sort((a, b) => b[2] - a[2]);
  medals.sort((a, b) => b[1] - a[1]);

  let copiedMedals = medals.map((el) => el.slice());
  copiedMedals.map((el) => el.shift());
  //  copiedMedals=   [
  //   [ 5, 0, 1 ],
  //   [ 1, 2, 0 ],
  //   [ 0, 3, 4 ],
  //   [ 0, 3, 0 ],
  //   [ 0, 1, 0 ],
  //   [ 0, 1, 0 ],
  //   [ 0, 0, 1 ]
  // ]

  let cnt = 0;

  // 배열 값 비교를 위해 깊은 복사 ( 2차원 배열이라..)
  for (let i = 0; i < medals.length; i++) {
    if (
      JSON.stringify(copiedMedals[i]) === JSON.stringify(copiedMedals[i + 1])
    ) {
      medals[i + 1].push(cnt + 1);
      cnt++;
    }
  }
  // medals = [
  //   [ 5, 5, 0, 1 ],
  //   [ 1, 1, 2, 0 ],
  //   [ 7, 0, 3, 4 ],
  //   [ 6, 0, 3, 0 ],
  //   [ 2, 0, 1, 0 ],
  //   [ 3, 0, 1, 0, 1 ],
  //   [ 4, 0, 0, 1 ]
  // ];

  result = 0;
  for (let i = 0; i < medals.length; i++) {
    if (medals[i].length === 4) {
      if (medals[i][0] === k) {
        result = i + 1;
        break;
      }
    } else {
      result = i + 1 - medals[i][medals[i].length - 1];
    }
  }
  return result;
}

